package com.buggyarts.showcase.models;


public class ShowcaseImage {

    public String name;
    public String url;

    public ShowcaseImage(){}

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
